static int popcount(uint8_t x)
{
    int ret = 0;
    do {
        ret += x & 1;
    } while (x >>= 1);
    return ret;
}
static const float k1 = 0.01;
static const float k2 = 0.03;
static float dssim(uint8_t *x, uint8_t *y)
{
    const float c1 = k1*k1;
    const float c2 = k2*k2;
    float mux = 0, muy = 0;
    float sigx, sigy, sigxy = 0;
    float res;
    int i;

    for (i = 0; i < 8; i++) {
        mux   += popcount(*x);
        muy   += popcount(*y);
        sigxy += popcount(*x & *y);
        x++, y++;
    }
    mux /= 8*8;
    muy /= 8*8;
    sigxy /= 8*8;
    sigx = mux - mux*mux;
    sigy = muy - muy*muy;
    sigxy = sigxy - mux*muy;
    res = ((2*mux*muy + c1)*(2*sigxy + c2)) / ((mux*mux + muy*muy + c1)*(sigx*sigx + sigy*sigy + c2));

//    printf("%8.6f : mux %9.6f muy %9.6f sigx %9.6f sigy %9.6f sigxy %9.6f", res, mux, muy, sigx, sigy, sigxy);

    return (1 - res) / 2;
}

static float sse(uint8_t *x, uint8_t *y)
{
    float res = 0;
    int i;

    for (i = 0; i < 8; i++) {
        res += popcount(*x ^ *y);
        x++, y++;
    }

    return res;
}

static float sse4(uint8_t *x, uint8_t *y)
{
    float res = 0;
    uint8_t vx[4] = { 0 };
    uint8_t sx = 0;
    uint8_t vy[4] = { 0 };
    uint8_t sy = 0;
    int diff;
    int i;

    for (i = 0; i < 4; i++) {
        vx[0] += popcount(*x&0xf0);
        vx[1] += popcount(*x&0x0f);
        vy[0] += popcount(*y&0xf0);
        vy[1] += popcount(*y&0x0f);
        sx += popcount(*x);
        sy += popcount(*y);
        x++, y++;
    }
    for (i = 4; i < 8; i++) {
        vx[2] += popcount(*x&0xf0);
        vx[3] += popcount(*x&0x0f);
        vy[2] += popcount(*y&0xf0);
        vy[3] += popcount(*y&0x0f);
        sx += popcount(*x);
        sy += popcount(*y);
        x++, y++;
    }

    for (i = 0; i < 4; i++) {
        diff  = vx[i]-vy[i];
        diff *= diff;
        res += diff;
    }
    res *= 2;
    diff  = sx - sy;
    diff *= diff;
    res += diff;

    return res;
}

