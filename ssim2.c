static int popcount16(uint16_t x)
{
    int ret = 0;
    do {
        ret += x & 1;
    } while (x >>= 1);
    return ret;
}

static int popcount(uint8_t x)
{
    int ret = 0;
    do {
        ret += x & 1;
    } while (x >>= 1);
    return ret;
}
static const float k1 = 0.01;
static const float k2 = 0.03;
static float dssim(uint8_t *x, uint8_t *y)
{
    const float c1 = k1*k1;
    const float c2 = k2*k2;
    float mux = 0, muy = 0;
    float sigx, sigy, sigxy = 0;
    float res;
    int i;

    for (i = 0; i < 8; i++) {
        mux   += popcount(*x);
        muy   += popcount(*y);
        sigxy += popcount(*x & *y);
        x++, y++;
    }
    mux /= 8*8;
    muy /= 8*8;
    sigxy /= 8*8;
    sigx = mux - mux*mux;
    sigy = muy - muy*muy;
    sigxy = sigxy - mux*muy;
    res = ((2*mux*muy + c1)*(2*sigxy + c2)) / ((mux*mux + muy*muy + c1)*(sigx*sigx + sigy*sigy + c2));

//    printf("%8.6f : mux %9.6f muy %9.6f sigx %9.6f sigy %9.6f sigxy %9.6f", res, mux, muy, sigx, sigy, sigxy);

    return (1 - res) / 2;
}

static float sse(uint8_t *x, uint8_t *y)
{
    float res = 0;
    int i;

    for (i = 0; i < 8; i++) {
        res += popcount(*x ^ *y);
        x++, y++;
    }

    return res;
}

static float sse4(uint16_t *x, uint16_t *y, int w)
{
	float res = 0;
	uint16_t vx[4] = { 0 };
	uint16_t sx = 0;
	uint16_t vy[4] = { 0 };
	uint16_t sy = 0;
	int diff;
	int i;

	for (i = 0; i < (LINES/2); i++) {
//printf("x %03x|%03x y %03x|%03x\n", (*x&0x03e0)>>5, *x&0x001f, (*y&0x03e0)>>5, *y&0x001f);
		vx[0] += popcount16(*x&0x03e0);
		vx[1] += popcount16(*x&0x001f);
		vy[0] += popcount16(*y&0x03e0);
		vy[1] += popcount16(*y&0x001f);
		sx += popcount16(*x);
		sy += popcount16(*y);
		y += w;
		x++;
	}
//printf("\n");
	for (i = (LINES/2); i < LINES; i++) {
//printf("x %03x|%03x y %03x|%03x\n", (*x&0x03e0)>>5, *x&0x001f, (*y&0x03e0)>>5, *y&0x001f);
		vx[2] += popcount16(*x&0x03e0);
		vx[3] += popcount16(*x&0x001f);
		vy[2] += popcount16(*y&0x03e0);
		vy[3] += popcount16(*y&0x001f);
		sx += popcount16(*x);
		sy += popcount16(*y);
		y += w;
		x++;
	}
//printf("\n");

	for (i = 0; i < 4; i++) {
		diff  = vx[i]-vy[i];
		diff *= diff;
		res += diff;
	}
	res *= 2;
	diff  = sx - sy;
	diff *= diff;
	res += diff;

	return res;
}

